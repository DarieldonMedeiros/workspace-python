"""
DocString (Uma string para documentação)
Python  = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
    * Dinâmica: Já detecta qual tipo de dado está sendo colocado
    * Forte: não realiza conversões automaticamente
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Darieldon Medeiros')

# Aspas duplas
print("Darieldon Medeiros")
print(1, 'Darieldon "Medeiros"')

# Escape (Utiliza barra invertida "\" para utilizar o escape)
print("Darieldon \"Medeiros")
print(2, "Darieldon 'Medeiros'")

# r (r mostra os caracteres de escape)
print(r"Darieldon \"Medeiros")

