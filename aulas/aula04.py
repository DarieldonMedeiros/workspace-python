
# Tipos int e float
#   * int -> número inteiro.
#   * O tipo int representa qualquer número positivo ou negativo. O int sem sinal é considerado positivo (+).
print(11) # int
print(-11) # int
print(0)

#   * float -> número com ponto flutuante
#   * O tipo float representa qualquer número positivo ou negativo com ponto flutuante. O float sem sinal é considerado positivo (+).
print(1.1, 10.11) # utilize os pontos no lugar de vírgula
print(0.0, -1.5)

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Darieldon'))
print(type(1))
print(type(-1))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))