'''
Operadores de comparação (relacionais)
OP       Significado         EXEMPLO (True)
>        maior               2 > 1
>=       maior ou igual      2 >= 2
<        menor               1 < 2
<=       menor ou igual      2 <= 2
==       igual               'a' == 'a'
!=       diferente           'a' != 'b'
'''

maior = 2 > 1
maiorOuIgual = 2 >= 2
menor = 1 < 2
menorOuIgual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(maior)
print(maiorOuIgual)
print(menor)
print(menorOuIgual)
print(igual)
print(diferente)